//OPERATORS
//arithmetic operators
let x = 10;
let y = 5;
let z = x + y; // addition
let a = x - y; // subtraction
let b = x * y; // multiplication
let c = x / y; // division
let d = x % y; // modulus 
console.log(z); // Output: 15
console.log(a); // Output: 5
console.log(b); // Output: 50
console.log(c); // Output: 2
console.log(d); // Output: 0

//comparison operators
let e = x===y; 
let f = x !==y;
let g = x > y;
let h = x < y;
let i = x >= y;
let j = x <= y;
console.log(e);
console.log(f);
console.log(g);
console.log(h);
console.log(i);
console.log(j);

//assignment operators
let k = 50;
let l = k += 5; // k = k + 5
let m = k -= 10; // k = k - 10
let n = k *= 2; // k = k * 2
let o = k /= 5; // k = k / 5
console.log(l);
console.log(m);
console.log(n);
console.log(o);
