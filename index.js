//DATA TYPES IN JAVASCRIPT

//PRIMITIVE DATA TYPES
//String : a sequence of characters enclosed in single or double quotes
//var , let , const are used to declare variables in JavaScript
var person = "John Doe"; //String
console.log(person); // Output: John Doe
let myName = "Ajibola";
console.log(myName); // Output: Ajibola

//Number : represents both integer and floating-point numbers in JavaScript
let age = 30;
const pi = 3.14;
console.log(age); // Output: 30
console.log(pi); // Output: 3.14    

//Boolean : represents a logical entity that can have two values: true or false
let isStudent = true;
const isMarried = false;
console.log(isStudent); // Output: true
console.log(isMarried); // Output: false